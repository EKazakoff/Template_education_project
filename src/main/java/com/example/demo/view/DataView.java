package com.example.demo.view;

/**
 * The type Data view.
 */
public class DataView {

    private Object data;

    /**
     * Gets data.
     *
     * @return the data
     */
    public Object getData() {
        return data;
    }

    /**
     * Sets data.
     *
     * @param data the data
     */
    public void setData(Object data) {
        this.data = data;
    }

    /**
     * Instantiates a new Data view.
     *
     * @param data the data
     */
    public DataView(Object data) {
        this.data = data;
    }
}
